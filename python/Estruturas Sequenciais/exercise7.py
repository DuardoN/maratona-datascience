# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
import math
lado = float(input('Por favor informe um lado do quadrado: '))
area_quadrado = lado * lado
print('A area desse quadrado é: ', area_quadrado)
print('O dobro dessa area é: ', area_quadrado * 2)
# Faça um Programa que peça as 4 notas bimestrais e mostre a média.
nota1 = input('Por favor a nota do primeiro Bimestre: ')
nota2 = input('Por favor a nota do segundo Bimestre: ')
nota3 = input('Por favor a nota do terceiro Bimestre: ')
nota4 = input('Por favor a nota do quarto Bimestre: ')
media = (nota1 + nota2 + nota3 + nota4) / 4
print('Sua média final é', media)